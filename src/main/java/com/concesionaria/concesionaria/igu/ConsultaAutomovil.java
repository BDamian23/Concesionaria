    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        //Control de que la tabla no este vacia
        if(tablaDatos.getRowCount() > 0){
            //Valido que se haya seleccionado un registro
            if(tablaDatos.getSelectedRow() != -1){
                //Obtener id
                int id =  Integer.parseInt(String.valueOf(tablaDatos.getValueAt(tablaDatos.getSelectedRow(), 0)));
                control.eliminarAuto(id);
                cargarTabla();
                JOptionPane.showMessageDialog(null, "Automovil Eliminado Correctamente!");
            } else {
                JOptionPane.showMessageDialog(null, "Indica la fila a eliminar!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay datos en la tabla!");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

